package datatypes;

import java.util.Arrays;

public class ForDemo1 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; ++i) {
            System.out.println(i);
        }
        System.out.println("=======================");
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

    }
}
